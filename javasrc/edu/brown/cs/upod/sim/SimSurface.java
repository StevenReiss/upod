package edu.brown.cs.upod.sim;

import java.awt.Color;
import java.awt.geom.Rectangle2D;



class SimSurface extends SimObject
{


/********************************************************************************/
/*                                                                              */
/*      Private Storage                                                         */
/*                                                                              */
/********************************************************************************/



/********************************************************************************/
/*                                                                              */
/*      Constructors                                                            */
/*                                                                              */
/********************************************************************************/

SimSurface()
{
   super("BASIC_SURFACE","Ground underlying everything");
   
   Rectangle2D rect = new Rectangle2D.Double(-2000,-2000,4000,4000);
   SimShape shp = SimShape.createPrism(rect,-0.1,-0.1,false);
   shp.setColors(new SimColorSet(new Color(127,255,212)));
   addShape(shp);
}



}       // end of class SimSurface




