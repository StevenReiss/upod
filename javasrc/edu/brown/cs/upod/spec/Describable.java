package edu.brown.cs.upod.spec;



import javax.swing.Icon;


/**
 *	This interface describes the calls that any object that needs to be
 *	accessed by the user interface should provide.
 *
 **/


public interface Describable {


/**
 *	Return a short name for this object.  This is typically designed to
 *	be used in a menu or button or label.
 **/

String getName();


/**
 *	Return a detailed description of this action.  This is typically
 *	designed to be used for a tool tip or other explanation.  It is
 *	given in html notation to allow formatting.
 **/

String getDescription();



/**
 *	Return an icon for this object.  This can return null.
 **/

Icon getIcon();



}	// end of interface Describable



